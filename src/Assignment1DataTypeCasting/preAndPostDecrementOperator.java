package Assignment1DataTypeCasting;

public class preAndPostDecrementOperator {
    public static void main(String[] args) {
        int a=10;
        int b=10;
        //For post decrement operator
        System.out.println("The value of the variable at the start is "+a);
        System.out.println("using post decrement "+a--);
        System.out.println("Value of the variable after post decrement is "+a);
        System.out.println("**********************");
        //For pre decrement operator
        System.out.println("The value of the variable at the start is "+b);
        System.out.println("using post decrement "+--b);
        System.out.println("Value of the variable after post decrement is "+b);
    }
}
